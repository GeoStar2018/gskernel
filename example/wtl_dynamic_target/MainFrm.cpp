LRESULT CMainFrame::OnFileOpen(WORD /*wNotifyCode*/, WORD /*wID*/, HWND /*hWndCtl*/, BOOL& /*bHandled*/)
{
	CMultiFileDialog f(_T("*.fcs\0*.fcs\0\0"));
	if (IDOK != f.DoModal())
		return 0;
	std::vector<std::string> vec;

	const char* str = f.GetFirstFileName();
	while (str)
	{
		vec.push_back(GeoStar::Utility::GsStringHelp::Replace(str,".fcs",""));
		str = f.GetNextFileName();
	}
	char dir[128];
	f.GetDirectory(dir,128);

	m_view.OpenMap(dir, vec);
	return 0;

}
